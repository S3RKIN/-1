class BooleanTesting {
    public static void main(String args[]) {
        boolean bool;
        bool = false;
        System.out.print(bool);
        bool = true;
        System.out.print(bool);
        if (bool)
            System.out.print(bool);
        bool = false;
        if (bool)
            System.out.print(bool);
    }
}